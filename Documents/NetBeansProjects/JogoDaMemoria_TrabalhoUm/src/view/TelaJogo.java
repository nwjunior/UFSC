    @Override
    public void mousePressed(MouseEvent e) {
        if(e.getSource() instanceof JButton){
            JButton btnAux = (JButton)e.getSource();
            if(btnAux.isEnabled()){
                 btnAux.setEnabled(false);
                 clicou = true;
            }
        }
    }   

    @Override
    public void mouseReleased(MouseEvent e) {
       if(clicou){
           if(e.getSource() instanceof JButton){
                JButton btnAux = (JButton)e.getSource();
                this.release(btnAux);
                clicou = false;
            }
        }
    }

