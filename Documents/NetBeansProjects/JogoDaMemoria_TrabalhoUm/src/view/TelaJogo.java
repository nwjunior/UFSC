    private void delay(int time){
        try {
        TimeUnit.SECONDS.sleep(time);//new TelaJogoErrada();//while(System.currentTimeMillis() < inicio + 10000){}
        } catch (InterruptedException ex) {
            Logger.getLogger(TelaJogo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void release(JButton btnAux){
        if(controle.isSegundoClick()){
            controle.enviaClick(btnAux.getName());
            if(!controle.cartasIguais()){
                this.delay(1);
                JButton btnPrimeiroClick = componentes.get(controle.getIdPrimeiroClick());
                JButton btnSegundoClick = componentes.get(controle.getIdSegundoClick());
                btnPrimeiroClick.setEnabled(true);
                btnSegundoClick.setEnabled(true);
            }
            else{
                controle.atualizaQtdCartasViradas();
            }
            this.jLabel1.setText(String.valueOf(controle.getQuantidadeJogadas()));
            if(controle.vencedor()){
                this.dispose();
                controle.setFim(System.currentTimeMillis());
                controle.adicionarAoRankDeJogadas();
                controle.adicionarAoRankDeTempos();
                new TelaExibicaoFinal(controle.getQuantidadeJogadas(), controle.getDuracaoPartida()).setVisible(true);
            }
        }
        else{
            controle.enviaClick(btnAux.getName());
        }
    }

    
    
