    private void organizaComponentes(){
        Component[] componentesTela = getContentPane().getComponents();
        String[] caminhoImagens = this.controle.getImagensEmbaralhadas();
        int i = 0;
        while(i < caminhoImagens.length){
            if(componentesTela[i] instanceof JButton){
                JButton btnAux = (JButton) componentesTela[i];
                btnAux.setName(String.valueOf(i));
                btnAux.setIcon(new ImageIcon(getClass().getResource(CAMINHO_IMAGEM_FUNDO)));
                btnAux.setDisabledIcon(new ImageIcon(getClass().getResource(caminhoImagens[i])));
                btnAux.addMouseListener(this);
                componentes.put(String.valueOf(i), btnAux);
                i++;
            }
        }
    }
    

